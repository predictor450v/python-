'''we use NumPy because it lets us work with large numerical data faster, cleaner, and more efficiently than plain Python.

Key reasons (one-liners):

Speed: NumPy runs computations in optimized C, much faster than Python loops.

Arrays: Provides powerful multi-dimensional arrays (ndarray) for numerical data.

Vectorization: Perform operations on entire arrays without explicit loops.

Memory efficiency: Uses less memory than Python lists for numbers.

Math support: Built-in linear algebra, statistics, and numerical functions.

Foundation: Most data science libraries (Pandas, SciPy, ML frameworks) depend on NumPy.'''